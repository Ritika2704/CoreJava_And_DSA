package Collection;

//Collection stores only object (not primitive data)
public class WapperClass {
    public static void main(String[] args) {

        //Autoboxing
        int x= 23;
        Integer obj = x;
        System.out.println(obj);

        //AutoUnboxing
        Integer obj1 = 35;
        int Z= obj1;
        System.out.println(Z);
    }

}
